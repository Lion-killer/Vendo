// Спільний порядок за назвою (#61): українська абетка, регістронезалежно, натуральний
// порядок чисел («Товар 2» перед «Товар 10»). Колатор створюємо один раз — це помітно
// дешевше за localeCompare на кожній парі для великих списків каталогу/контрагентів.
// Порожня назва трактується як "" (у кінці за абеткою — у практиці назви завжди є).
const collator = new Intl.Collator('uk', { numeric: true, sensitivity: 'base' });
export const byName = (a, b) => collator.compare(a?.name || '', b?.name || '');

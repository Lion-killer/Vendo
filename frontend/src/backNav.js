// Перехоплювачі апаратного «Назад» (#71). Дерева (каталог/клієнти), поки заглиблені в
// підгрупу, реєструють функцію, що піднімає їх на рівень угору; App-обробник backButton
// викликає runBack() ПЕРЕД зміною екрана (за зразком closeLightbox). Найпізніше
// зареєстрований спрацьовує першим (LIFO) — вкладений перехоплювач має пріоритет над
// зовнішнім. Перехоплювач повертає true, якщо «поглинув» «Назад» (піднявся на рівень).

const handlers = [];

// Зареєструвати перехоплювач; повертає функцію відписки (для cleanup у useEffect).
export const registerBack = (fn) => {
  handlers.push(fn);
  return () => { const i = handlers.indexOf(fn); if (i >= 0) handlers.splice(i, 1); };
};

// Викликати перехоплювачі від найновішого; перший, що поверне true, зупиняє пошук.
// Повертає true, якщо «Назад» оброблено деревом (екран міняти не треба).
export const runBack = () => {
  for (let i = handlers.length - 1; i >= 0; i--) if (handlers[i]()) return true;
  return false;
};
